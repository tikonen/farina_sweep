function ret = generate_r40(limit)
  % Generate renard numbers up to a limit
  r40 = renard_r40();
  ret = r40;
  decade = 10;
  while ret(end) < limit
    ret = [ret r40(1:end-1) * decade];
    decade *= 10;
  endwhile

  % Limit
  for i=length(ret):-1:1
    if ret(i) < limit
      ret = ret(1:i);
      break
    endif
  endfor
  ret = unique(round(ret));
endfunction
